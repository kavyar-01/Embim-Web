    <script>
    (function () {
        const fab       = document.getElementById('contact-fab');
        const dropdown  = document.getElementById('contact-dropdown');
        const iconChat  = document.getElementById('fab-icon-chat');
        const iconClose = document.getElementById('fab-icon-close');
        let isOpen = false;

        function openDropdown() {
            dropdown.classList.remove('hidden');
            // Trigger transition on next frame
            requestAnimationFrame(() => {
                dropdown.classList.remove('translate-y-2', 'opacity-0');
                dropdown.classList.add('translate-y-0', 'opacity-100');
            });
            iconChat.classList.add('hidden');
            iconClose.classList.remove('hidden');
            fab.classList.add('rotate-0');
            isOpen = true;
        }

        function closeDropdown() {
            dropdown.classList.remove('translate-y-0', 'opacity-100');
            dropdown.classList.add('translate-y-2', 'opacity-0');
            // Wait for transition to finish before hiding
            setTimeout(() => {
                dropdown.classList.add('hidden');
            }, 250);
            iconChat.classList.remove('hidden');
            iconClose.classList.add('hidden');
            isOpen = false;
        }

        fab.addEventListener('click', function (e) {
            e.stopPropagation();
            isOpen ? closeDropdown() : openDropdown();
        });

        // Click outside to close
        document.addEventListener('click', function (e) {
            if (isOpen && !dropdown.contains(e.target) && !fab.contains(e.target)) {
                closeDropdown();
            }
        });

        // ESC key to close
        document.addEventListener('keydown', function (e) {
            if (e.key === 'Escape' && isOpen) closeDropdown();
        });
    })();
